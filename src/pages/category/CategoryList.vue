<template>
  <div>
    <div class="form-control">
 
        <div class="buttons">
          <router-link to="category-add">
            <el-button plain @click="handleToCateAdd" >新增</el-button>
          </router-link>
          <el-button plain @click="handleDelete()">删除</el-button>
        </div>
    

    </div>
      <el-col :span="18" class="mt20">
        <div class="custom-tree-container">
          <div class="block">
            <el-tree
              :data="data5"
              show-checkbox
              node-key="id"
              default-expand-all
              :expand-on-click-node="false"
            >
              <span class="custom-tree-node" slot-scope="{ node, data }">
                <span>{{ node.label }}</span>
                <span>
                  <el-button type="text" size="mini" @click="() => append(data)">Append</el-button>
                </span>
              </span>
            </el-tree>
          </div>
        </div>
      </el-col>
  </div>
</template>

<script>
let id = 1000;
export default {
  data() {
    const data = [
      {
        id: 1,
        label: "一级 1",
        children: [
          {
            id: 4,
            label: "二级 1-1",
            children: [
              {
                id: 9,
                label: "三级 1-1-1"
              },
              {
                id: 10,
                label: "三级 1-1-2"
              }
            ]
          }
        ]
      },
      {
        id: 2,
        label: "一级 2",
        children: [
          {
            id: 5,
            label: "二级 2-1"
          },
          {
            id: 6,
            label: "二级 2-2"
          }
        ]
      },
      {
        id: 3,
        label: "一级 3",
        children: [
          {
            id: 7,
            label: "二级 3-1"
          },
          {
            id: 8,
            label: "二级 3-2"
          }
        ]
      }
    ];
    return {
      data4: JSON.parse(JSON.stringify(data)),
      data5: JSON.parse(JSON.stringify(data))
    };
  },
  methods: {
    handleDelete(list = []) {
      if (list.length === 0) {
        return;
      }
      const arr = list.map(v => {
        return v.id;
      });
      this.$axios({
        method: "GET",
        url: `/admin/goods/del/${arr.join(",")}`
      }).then(res => {
        const { message } = res.data;
        this.$message({
          message,
          type: "success"
        });
        this.getList();
      });
    },
    append(data) {
      const newChild = { id: id++, label: "testtest", children: [] };
      if (!data.children) {
        this.$set(data, "children", []);
      }
      data.children.push(newChild);
    },

    remove(node, data) {
      const parent = node.parent;
      const children = parent.data.children || parent.data;
      const index = children.findIndex(d => d.id === data.id);
      children.splice(index, 1);
    },

    renderContent(h, { node, data, store }) {
      return (
        <span class="custom-tree-node">
          <span>{node.label}</span>
          <span>
            <el-button
              size="mini"
              type="text"
              on-click={() => this.append(data)}
            >
              Append
            </el-button>
            <el-button
              size="mini"
              type="text"
              on-click={() => this.remove(node, data)}
            >
              Delete
            </el-button>
          </span>
        </span>
      );
    },
    handleToCateAdd(){
      this.$router.push("/admin/category-add");
    }
  }
};
</script>

